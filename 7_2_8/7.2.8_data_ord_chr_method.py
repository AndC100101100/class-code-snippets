'''
we are using ord() function which return an integer
representing the Unicode code point of a single character
'''
print(ord('A')) # should be 65 in denary
print(ord('B')) # 66 in denary
print(ord('C')) # 67 in denary

'''
we are using chr() function which converts an integer into
its corresponding Unicode character
'''
print(chr(65)) # A
print(chr(66)) # B
print(chr(67)) # C

