misc = ['purple', 99, 3.14, False, ['apple', 'orange', 'pear']] 
print(misc[1])
print(misc[4])
print(misc[4][0]) # will print out the first position of the array inside of our array

'''
you can also pick out individual letters from a string this way
as we know that a string is a list of characters
'''
size = 'large'
print(size[2])

print(size[2:4]) # we can pull a substring from our main string
