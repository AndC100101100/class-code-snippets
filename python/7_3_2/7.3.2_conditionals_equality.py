letter_A = 'A'

print(3 == 3)          # True
print(1 == 2)          # False
print(0 == False)      # True (0 is Falsy, matches False)
print(1 == False)      # False
print('A' == 'A')      # True
print('A' == 'a')      # False
print('A' == letter_A) # True ('A' matches 'A')
print('A' == 1)        # False
