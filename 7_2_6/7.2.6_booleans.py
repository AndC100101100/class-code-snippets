print(bool('123'))      # True     string with a value
print(bool(''))         # False    string with empty value
print(bool(123))        # True     integer with a value
print(bool(123.45))     # True     float with a value
print(bool(2))          # True     integer with a value
print(bool(1))          # True     integer with a value
print(bool(0))          # False    integer with zero value
print(bool('0'))        # True     string with a value
print(bool(-1))         # True     integer with a value
print(bool('True'))     # True     string with a value
print(bool('False'))    # True     string with a value
print(bool(True))       # True     boolean with a True value
print(bool(False))      # False    boolean with a False value
